function calculateDiscountedPrice(quantity, pricePerItem) {
    let totalPrice = 0;

    for (let i = 0; i < quantity; i++) {
        totalPrice += pricePerItem;
    }

    if (quantity >= 10) {
        totalPrice *= 0.9; // apply 10% discount
    }

    return totalPrice;
}


module.exports = calculateDiscountedPrice;